#include <stdio.h>
#include <stdlib.h>

int main()
//Numero de 0 a 10
{
    int cont;


    cont = 0; //inicialiazação da variavel contadora
    while (cont <= 10)//teste do laço de repetição
    {
        printf("%d \n",cont);


        cont++;//incremento da variavel contadora
    }

    return 0;
}
